## Lambda Fxn ##
## Labda fxn -> Small anonymous function ,  take any number of arguments but can only have one expression
## Syntax -> labda arguments : expression

x = lambda a: a+10
print(x(5))   # 5= argument   a=5 i.e a+10=15
print()

y= lambda c,d: c*d
print(y(5,6))
print()

## Best use = use lambda inside fxn
## When you need a short and simple function just for temporary use.
## When returning a function from another function.
